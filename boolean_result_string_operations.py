my_str = "Python"

# 1. startswith()
print(my_str.startswith('P'))
print(my_str.startswith('Pyt'))

# 2. endswith()
print(my_str.endswith('n'))
print(my_str.endswith('en'))

# 3. islower()
print(my_str.islower())

# 4. isupper()
print(my_str.isupper())

# 5. istitle()
print(my_str.istitle())

# 6. isspace()
print(my_str.isspace())

# 7. isalpha() (to check that string contains alphabets only or not)
print(my_str.isalpha())

# 8. isnumeric()
print(my_str.isnumeric())